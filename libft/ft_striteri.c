#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	char	*c;
	int		i;

	i = 0;
	if (s != NULL && f != NULL)
	{
		while (s[i] != '\0')
		{
			c = &s[i];
			f(i, c);
			++i;
		}
	}
}
